package com.lite_k8s.update;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import static org.assertj.core.api.Assertions.*;

class ImageWatchEntityTest {

    @Test
    @DisplayName("1. ImageWatchEntityлҘј мғқм„ұн•ҳл©ҙ idк°Җ UUIDлЎң мһҗлҸҷ мғқм„ұлҗңлӢӨ")
    void idIsAutoGenerated() {
        ImageWatchEntity entity = new ImageWatchEntity();
        assertThat(entity.getId()).isNotNull();
        assertThat(entity.getId()).hasSize(36); // UUID format
    }

    @Test
    @DisplayName("2. ImageWatchEntityлҠ” image, tag, containerPattern, maxUnavailable н•„л“ңлҘј к°Җм§„лӢӨ")
    void hasRequiredFields() {
        ImageWatchEntity entity = ImageWatchEntity.builder()
                .image("ghcr.io/silicao3o/exercise-auth")
                .tag("v1.0")
                .containerPattern("exercise-auth.*")
                .maxUnavailable(2)
                .build();

        assertThat(entity.getImage()).isEqualTo("ghcr.io/silicao3o/exercise-auth");
        assertThat(entity.getTag()).isEqualTo("v1.0");
        assertThat(entity.getContainerPattern()).isEqualTo("exercise-auth.*");
        assertThat(entity.getMaxUnavailable()).isEqualTo(2);
    }

    @Test
    @DisplayName("3. ImageWatchEntityлҠ” enabled, createdAt н•„л“ңлҘј к°Җм§„лӢӨ")
    void hasEnabledAndCreatedAt() {
        ImageWatchEntity entity = new ImageWatchEntity();
        assertThat(entity.isEnabled()).isTrue(); // кё°ліёк°’ true
        assertThat(entity.getCreatedAt()).isNotNull();
    }

    @Test
    @DisplayName("4. ImageWatchEntityм—җ imageк°Җ м—Ҷмңјл©ҙ validateм—җм„ң мҳҲмҷёк°Җ л°ңмғқн•ңлӢӨ")
    void validateFailsWithoutImage() {
        ImageWatchEntity entity = ImageWatchEntity.builder()
                .image(null)
                .containerPattern("test.*")
                .build();

        assertThatThrownBy(entity::validate)
                .isInstanceOf(IllegalStateException.class)
                .hasMessageContaining("image");
    }

    @Test
    @DisplayName("5. ImageWatchEntityмқҳ tag кё°ліёк°’мқҖ latestмқҙлӢӨ")
    void defaultTagIsLatest() {
        ImageWatchEntity entity = new ImageWatchEntity();
        assertThat(entity.getTag()).isEqualTo("latest");
    }

    @Test
    @DisplayName("ImageWatchEntityм—җ ghcrToken н•„л“ңк°Җ мһҲлӢӨ (кё°ліё null)")
    void hasGhcrTokenField() {
        ImageWatchEntity entity = new ImageWatchEntity();
        assertThat(entity.getGhcrToken()).isNull();

        ImageWatchEntity withToken = ImageWatchEntity.builder()
                .image("ghcr.io/org/app")
                .ghcrToken("ghp_abc123")
                .build();
        assertThat(withToken.getGhcrToken()).isEqualTo("ghp_abc123");
    }
}
